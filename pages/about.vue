    Laboratory实验室注册地选择在香港，主要是源于两个方面的考虑：1.香港是自由汇率和完善的法治环境；2.香港的公司面向全球化的竞争环境，在税收方面有“一年报一次税”和“不盈利，不缴纳税收”的优势。考虑到我的家庭经济环境，最终在2015年3月份决定把实验室注册在香港。
    就在我打算将实验室注册的时间段，也就是2015年1月份左右，农历新年前一个礼拜，我的母亲突发脑溢血去世。

    对于家庭，对于父母，我有很多的亏欠。之后，我对实验室的注册、运作和管理等父母有了很多的调整，一方面我有勇气去创业，另一方面，我想给去世的妈妈一个比较好的交代。更重要的是，我想让爸爸看看，想让他知道爸爸妈妈对我的家庭教育和在生活方面的付出是值得的。
