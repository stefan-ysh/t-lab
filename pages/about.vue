    （2）Jiabin Science Laboratory的OTL和OTM模式

    OTL（Office of Technology Licensing），即技术许可办公室。20世纪80年代，美国在《拜杜法案》（《Bayh Dole
    Act》）的推进中，实施与管理专利经营的商业活动，不同于一般意义上的科研管理，设立了专门机构。
    通常统一管理学校的知识产权成果，包括发明的审查、专利的申请、知识产权的转让与许可、股权的管理等。一项科学与技术通过发明专利公开之后，关键经营权（是否申请专利，寻找合适的企业，以及专利许可协议的谈判等）都掌握在技术经理人手中，而专利申请的具体事宜，学校通常委托校外专利事务所办理。

    硅谷（Silicon Valley）的OTM（Office of Technology
    Manager）模式，类似于国内高校的专利与知识产权办公室，也就是大学里边的专利办公室，或者叫知识产权办公室。这个制度非常灵活，斯坦福大学对技术转移收入扣除15%的管理费和申请专利的费用，余下“三三制”，也就是各三分之一（1/3）分给学校相关的所在学院、所在系和发明人。在斯坦福，教授们对外合作经费的40%可以用作自己的劳务收入，叫“智力支出”。

    Jiabin Science Laboratory（嘉宾科学实验室）通过OTM 技术经理（Office of Technology
    Manager），全权负责高校课题组发明专利的审查，以及了解知识产权成果的市场潜力。最终，在产权激励（The Incentive System of Property Rights）方面做一些事情。 Jiabin Science
    Laboratory（嘉宾科学实验室）为避免利益冲突（conflicts of
    interest），规避专利发明人（知识产权拥有者）不能参加OTL与企业之间的专利许可谈判。由于很多专利发明人集多重身份（教师、专利许可收入的分享者、公司顾问和公司董事）于一身，因此谈判由OTM 技术经理（Office of
    Technology Manager）全权代表高校课题组。 从Jiabin Science Laboratory实验室孵化的高科技公司和Jiabin Science Laboratory是合作平等关系，二者关系密切。

    实验室定期为孵化高科技公司产品更新提供技术支持和技术研发，孵化的高科技公司盈利后按照股份的不少于25%（>25%）分配Jiabin Science Laboratory（嘉宾科学实验室）以作技术支持的酬金。同时，Jiabin
    Science Laboratory实验室可以定期检测孵化的高科技公司的技术水平，实验室确保孵化的而高科技公司能够自主和平稳的运作，以及管理。从使用孵化的高科技公司法人代表同时兼任Jiabin Science
    Laboratory研究员，有义务为实验室提供技术帮助和资金支持。同时，高科技公司法人有权力享受Jiabin Science Laboratory的项目和产品研发以用作高科技公司的产品使用。

    Jiabin Science Laboratory实验室的宗旨和文化是“Knowledge Worth Spreading”，即“传播知识，分享生活”。 我们倾听我们的客户。

    三、研究生篇

    我是一名“85后”，2004年的第一次高考落榜，到后来的读技校和工厂工作，再到之后的2011年的第二次高考时，我的年龄已经25岁。2016那年离开五年的本科院校天津职业技术师范大学，以30岁大龄考取西安交通大学机械工程学院硕士研究生。西安交通大学以其优良的学习风气、严谨的科研氛围教我求学，以其包容的心态鼓励我创新，以其博大宽容的情怀胸襟和充实的校园生活育我成为一名社会人。同时，在我攻读硕士学位期间，作为一所研究型大学的教育而言，西安交通大学给予了我很多科研能力训练的机会，以及教我做人和做事情的方式与方法。（声明：部分文字素材内容截取自硕士毕业论文中的“致谢”！）

    回想起在西安交通大学的这段时间里面，很多人和事都给我留下很深刻的印象。现在原本应该是规划三年的硕士学习和生活，却在这些美好的付出与坚持过程中提前一年结束。现在，我33岁了，两年的硕士学习和生活刚刚开始的同时，伴随着我的将是交大硕士生活和学习的提前结束。整个硕士生活和学习期间，交大的很多人和事都给予我很多思考和感悟。（声明：部分文字素材内容截取自硕士毕业论文中的“致谢”！）

    在此期间，不断尝试Cutting-edge
    Research与跨学科讨论Idea之间的事情，并多次提议与组织不同学科的本硕博与高校教授之间的Seminar和Workshop研讨会。慢慢的，对于Process工艺的理解越来越深刻，再次基础上，开始组建一个新的前沿科学与技术研究团队，并划归在“西安图谱云端信息技术有限公司”进行企业制度管理，完成商业决策在前沿科学技术与科研项目孵化阶段的事情。
    沿用以色列的“Sandwich Pattern”（三明治模式），组建前沿科学与技术的科研团队：一个研究团队需要和必须有一位医生/药师，一位技术工程师，一位运营和管理的人。

    开始在医学与工科之间尝试做一些事情，并重点讨论： （1）Process与Idea（Instinct？直觉来自于哪里？）； （2）Digital to Biological Converter（Style？）； （3）Prototype
    Size （Process？）； （4）Accuracy Rates（Probability)； （5）Data Storage（稳定性？时效性？）； （6）Potential Risks (Law !!!)； （7）Origin
    Idea（科研的“Inspire/启发点”是什么？）。

    对三种研究方式有了一些认识和了解： （1）Cutting-edge Research，被誉为“边缘学科”，还有一种解释是“交叉学科” （2）Advanced Technology，被称为“先进技术研究”，主要强调制造工艺领域。
    （3）Frontier Institute of Science and Technology，被誉为“前沿科学与技术研究”。 这三种科研与教学方式里面，我个人觉得最棒的是Cutting-edge
    Research！记得著名的YALE耶鲁大学就一直强调“边缘学科”的研究，而不止是“前沿科学与技术”的研究。“边缘学科的研究”意味着争取到某一个研究领域的前三是有可能的！不管是学生还是教授，对“边缘学科”没有研究与成果和贡献，是不会被“邀请”和“聘用”到YALE！
    这三种Idea的设计思路，基本上都是在“鼓励创新”和“鼓励创造力” 。

    Idea发展与积累历程： 2004-2008，技校，对“工艺”了解 2008-2011，企业，熟悉“制造工艺”与“设计” 2011-2016，本科，对“Cutting-edge
    Research”有一点点认识2016-2017，思考“什么是交叉学科？” 2017-2018，“前沿”和“交叉”都不是重点，这点认识是最大的收获
    2018-12-22，硕士，回到“2011-2016”，有点点理解“Cutting-edge Research”

