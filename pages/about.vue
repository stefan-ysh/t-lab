    暨实验室理念和文化

    一、技校和大学篇

    我叫许嘉宾，是Jiabin Science Laboratory（嘉宾科学实验室）Idea构思最初的创始人，也是Jiabin Science
    Laboratory（香港，2015年3月）和西安图谱云端信息技术有限公司（西安，2018年6月）创始人。 之前由于家庭经济和家庭关系等方面的影响原因，我无缘读大学。

    在2004年参加高考失利之后，我就读于西安工程技术（技师）学院。在读技校那几年里面，我坚持每周六日都去离学校步行1小时路程的西安嘉汇汉唐书城阅读书籍和资料。那时候，我想把节省下来的公交车乘车费用用来买一些《南方周末》、《21世纪经济报导》和《中国日报》等这样的实时信息报纸，完善自己在价值观、历史观和生活观念等方面的不足。
    我刚开始到书城阅读书籍和查阅资料的过程中，不太清楚自己的兴趣在什么地方。我属于不太聪明，资质平平的学生，一开始按照书城的书籍分类阅读。

    经过一段时间的阅读训练，没过2年，我可以适当的选取自己喜欢的学科领域进行有针对性的阅读。等到技校毕业的时候，我已经能够很清楚的明白自己的兴趣和专业发展方向是什么了。那几年，除了学习和到书城完善和提高自己的知识之外，我开始喜欢上音乐。把时间放到2015年的现在，仔细想想看，我的音乐素养和生活领域的价值观、人生观等都是那几年建立的。
    2007年年底技校毕业之后，我先后在江苏省昆山市和浙江省台州市黄岩区参加模具方面的工作。

    那时候，为了减轻父母的家庭经济负担，自己一方面工作，一方面大量的阅读技术方面的书籍和资料，为了之后赚取更多的工资做准备。
    2009年开始，我跟着中科院化学所某研究员，一位从日本留学回国的教授高级工程师开始从事高端科研设备的设计、加工与科研领域的部分工作。一开始，我从一个技校生很难过渡到尖端的科研领域，对于“科研”的工作方式很不明白。工作期间也经常犯错误，对科研工作的不理解，对知识的幼稚理解，以及对中国和世界范围内前沿科学技术的懵懂理解，让我开始重新考虑学习新的专业技术和知识的打算。

    从2009年到2011年之间的三年里面，我一方面完成某研究员交给的工作任务，一方面开始晚上有空的时候就学习有关于科学和技术方面的新知识和新技能。在那几年的科研工作方式训练过后，我对“科学”和“技术”有了更深刻的理解和感悟。我在北京参加科研工作大约3年之后，2011年6月份重新参加了全国高考，借助大学自主招生和单招考试制度，最终考取了天津职业技术师范大学五年制本科，材料成型与控制工程专业。从拿到录取通知书的那一刻起，我就重新开始实现了之前没有机会读大学的理想。
    从2011年一直到2015年的五年本科学习和生活过程中，我依然利用周六日和寒暑假期间的时间，从天津到北京的某研究员那边从事科研方面的工作。（技校-企业-本科-硕士-博士）

    目前，我已经能够很好地运用科研的工作方式和能力完成一些中小型的课题研究，并且科研独立的承担科研方面的工作，也能够完成英文文章的写作，以能够更好的体现科研的成果。

    二、Jiabin Science Laboratory篇

    我在构思“Jiabin Science Laboratory”实验室这个概念的时间，就是从2011年9月份进入大学开始的。对于实验室的创建过程，我已开始就知道很艰难。事实上证明，我理解的“艰难”还只是真的“艰难”的一小部分。

    从2011年9月份起，我开始查阅资料和反复的阅读相关技术类的书籍，对一直在思考的“知识”有了很多不同意义上的理解。对于实验室的创建而言，我在最初的想法只是打算解决“怎样将知识能够更好地传播出去？”这个概念。随着时间的推移，我在解决问题的思考过程中，不断地完善对概念的理解，最终萌生了创建“Jiabin
    Science Laboratory”实验室的想法，并开始撰写计划书和准备注册实验室。

