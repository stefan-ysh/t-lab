    Laboratory”实验室的注册和运作事宜。这期间，遇到好朋友老乡和校友师超的帮助。在际遇面前，使得我们两个人在创业领域的讨论和交流很多。师超是2011年6月份和我一起在陕西省西安市一起参加高考时候认识的，那一年天津职业技术师范大学从技本生源和单招高考方面，在陕西省录取了两个人，一个是我，一个是师超。我被材料成型与控制工程专业录取，师超被机械制造工艺教育专业录取。

